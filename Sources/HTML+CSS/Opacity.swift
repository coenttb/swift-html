import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func opacity(
        _ opacity: Properties.Opacity?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.Opacity.property, opacity?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}