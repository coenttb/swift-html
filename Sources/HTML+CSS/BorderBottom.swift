import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func borderBottom(
        _ borderBottom: Properties.BorderBottom?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.BorderBottom.property, borderBottom?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}