import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func overlay(
        _ overlay: Properties.Overlay?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.Overlay.property, overlay?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}