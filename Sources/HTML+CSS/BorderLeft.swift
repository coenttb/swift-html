import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func borderLeft(
        _ borderLeft: Properties.BorderLeft?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.BorderLeft.property, borderLeft?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}