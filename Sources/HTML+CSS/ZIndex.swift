import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func zIndex(
        _ zIndex: Properties.ZIndex?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.ZIndex.property, zIndex?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}