import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func d(
        _ d: Properties.D?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.D.property, d?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}