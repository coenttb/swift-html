import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func clip(
        _ clip: Properties.Clip?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.Clip.property, clip?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}