import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func top(
        _ top: Properties.Top?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.Top.property, top?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}