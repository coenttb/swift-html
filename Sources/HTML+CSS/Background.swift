import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func background(
        _ background: Properties.Background?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.Background.property, background?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}