import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func borderWidth(
        _ borderWidth: Properties.BorderWidth?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.BorderWidth.property, borderWidth?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}