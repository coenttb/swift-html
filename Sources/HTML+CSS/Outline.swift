import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func outline(
        _ outline: Properties.Outline?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.Outline.property, outline?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}