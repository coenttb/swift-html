import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func textBox(
        _ textBox: Properties.TextBox?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.TextBox.property, textBox?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}