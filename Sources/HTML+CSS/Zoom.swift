import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func zoom(
        _ zoom: Properties.Zoom?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.Zoom.property, zoom?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}