import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func height(
        _ height: Properties.Height?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.Height.property, height?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}