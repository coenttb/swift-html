import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func tableLayout(
        _ tableLayout: Properties.TableLayout?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.TableLayout.property, tableLayout?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}