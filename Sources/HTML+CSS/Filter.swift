import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func filter(
        _ filter: Properties.Filter?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.Filter.property, filter?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}