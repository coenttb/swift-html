import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func r(
        _ r: Properties.R?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.R.property, r?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}