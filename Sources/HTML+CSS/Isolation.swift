import CSS
import Foundation
import PointFreeHTML

extension HTML {
    @discardableResult
    public func isolation(
        _ isolation: Properties.Isolation?,
        media mediaQuery: MediaQuery? = nil,
        pre: String? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        inlineStyle(Properties.Isolation.property, isolation?.description, media: mediaQuery, pre: pre, pseudo: pseudo)
    }
}